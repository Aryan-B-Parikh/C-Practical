// Documentation: Find Area And Perimeter of a Rectangle.
// header file.

#include <stdio.h>

int main()
{

    // Variable Declaration.
    int length = 40, Width = 20, Area, Perimeter;

    // Calculate a Area Of Rectangle.
    Area = length * Width;
    printf("Your Area is :%d\n", Area);

    // Calculate a Perimeter of Rectangle.
    Perimeter = 2 * (length + Width);
    printf("Your Perimeter is : %d\n", Perimeter);

    printf("\n\n24CE070_ARYAN");
    return 0;
}
